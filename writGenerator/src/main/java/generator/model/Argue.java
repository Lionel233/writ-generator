package main.java.generator.model;

public class Argue {

	private Litigant litigant;
	private String arguement;

	public Argue() {
		super();
	}

	public Litigant getLitigant() {
		return litigant;
	}

	public void setLitigant(Litigant litigant) {
		this.litigant = litigant;
	}

	public String getArguement() {
		return arguement;
	}

	public void setArguement(String arguement) {
		this.arguement = arguement;
	}

}
